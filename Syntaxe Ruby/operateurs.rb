# Opérateurs arithmétiques

+   # Addition
-   # Soustraction
*   # Multiplication
/   # Division
%   # Modulo
**  # Exponentiation

# Opérateurs de comparaison

<   # Strictement inférieur à
>   # Strictement supérieur à
<=  # Inférieur ou égale à
>=  # Supérieur ou égale à
==  # Égale à
!=  # Différent de
<=> # Comparaison (retourne -1 si inferieur à, 0 si égale à et 1 si supérieur à)
=== # Utilisé dans la condition case, son comportement varie en fonction des classes des objets qu'il compare
    # nombres   : agit comme l'opérateur ==
    # string    : agit comme l'opérateur ==
    # regex     : vérifie si la chaîne correspond à l'expression régulière
    # range     : vérifie si la valeur donnée se situe dans la plage

# Opérateurs d'afectation

=   # Afectation
+=  # Incrémentation
-=  # décrémentation
*=  # opérateur d'assignation de multiplication
/=  # opérateur d'assignation de division
%=  # opérateur d'assignation de modulo
**= # opérateur d'assignation de puissance

# Opérateurs logiques

&&  # ET
||  # OU
!   # Négation

